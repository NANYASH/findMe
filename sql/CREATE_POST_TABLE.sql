CREATE TABLE POST(
  ID           BIGINT PRIMARY KEY,
  TEXT         VARCHAR(200) NOT NULL,
  DATE_POSTED  DATE NOT NULL,
  LOCATION     VARCHAR(250) NOT NULL,
  USER_POSTED_ID  BIGINT,
  FOREIGN KEY(USER_POSTED_ID) REFERENCES USER_TABLE(ID),
  USER_PAGE_ID  BIGINT,
  FOREIGN KEY(USER_PAGE_ID) REFERENCES USER_TABLE(ID),
  ID_USER_POSTED BIGINT REFERENCES USER_TABLE(ID));